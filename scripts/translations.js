/**
 * Language Manager
 */
const Lang = {
    current: 'en',

    init() {
        // Get saved language or default to English
        this.current = localStorage.getItem('sanmati_lang') || 'en';
        this.applyLanguage();
    },

    toggle() {
        this.current = this.current === 'en' ? 'mr' : 'en';
        localStorage.setItem('sanmati_lang', this.current);
        this.applyLanguage();
    },

    get(key) {
        return TRANSLATIONS[this.current][key] || TRANSLATIONS.en[key] || key;
    },

    applyLanguage() {
        // Update HTML lang attribute
        document.documentElement.lang = this.current === 'mr' ? 'mr' : 'en';

        // Update all translatable elements
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.dataset.i18n;
            el.textContent = this.get(key);
        });

        // Update placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.dataset.i18nPlaceholder;
            el.placeholder = this.get(key);
        });

        // Update language toggle button
        const toggleBtn = document.getElementById('langToggle');
        if (toggleBtn) {
            toggleBtn.textContent = this.current === 'en' ? 'मराठी' : 'Eng';
        }

        // Update page title
        document.title = this.current === 'mr'
            ? 'सन्मती सेल्स | प्रीमियम हार्डवेअर आणि सॅनेटरी वेअर'
            : 'Sanmati Sales | Premium Tools & Hardware';
    }
};
